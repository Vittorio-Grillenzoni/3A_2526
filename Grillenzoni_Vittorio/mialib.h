/**
 * Chiede un valore e lo restituisce se è compreso tra x e y ed è un multiplo di z
 * @param int valore x
 * @param int valore y
 * @param int valore z
 * @return numero filtrato
 */
int inputNumeroFiltrato(int _x, int _y, int _z);
/**
 * Stampa 3 numeri in ordine crescente
 * @param int valore x
 * @param int valore y
 * @param int valore z
 * @return numeri in ordine crescente
 */
int ordinaTreValori(int _x, int _y, int _z);
/**
 * 
 * @param int _x Primo valore
 * @param int _y Secondo valore
 * @return Area o perimetro a seconda del caso
 */
int determinaFigura(int _x, int _y);

/**
 * Inserimento controllato di un valore intero tra min e max estremi compresi
 * @param int Valore minimo accettato
 * @param int Valore massimo accettato
 * @return Valore inserito dall'utente valido e nel range min-max
 */
int inputValoreRange(int _min, int _max);

